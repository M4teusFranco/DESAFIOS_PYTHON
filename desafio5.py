num1 = float(input('Informe um número: ')) # Solicitando o num1
num2 = float(input('Informe outro número: ')) # Solicitando o num2

resultSum = num1 + num2 # Formula de Soma
resultSub = num1 - num2 # Formula de Subtração
resultMult = num1 * num2 # Formula de Multiplicação
resultDiv = num1 / num2 # Formula de Divisão
resultExp = num1 ** num2 # Formula de Exponenciação

# Imprimir os resultados na console
print(f'Soma: {resultSum}') # Resultado da Soma
print(f'Subtração: {resultSub}') # Resultado da Subtração
print(f'Multiplicação: {resultMult}') # Resultado da Multiplicação
print(f'Divisão: {resultDiv}') # Resultado da Divisão
print(f'Exponenciação: {resultExp}') # Resultado da Exponenciação