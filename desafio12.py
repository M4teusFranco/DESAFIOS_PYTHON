frutas = ['Maçã', 'Morango', 'Uva'] # Declaração de valores do array 'frutas'

vegetais = ['Brocólis', 'Alface', 'Cenoura'] # Declaração de valores do array 'vegetais'

# For loop iterando entre 'frutas' e 'vegetais' até o fim das arrays
for fruta in frutas:
    for vegetal in vegetais:
        print(f'Eu gosto de {fruta} e {vegetal}')