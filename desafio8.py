frutas = ['Maçã', 'Manga', 'Banana', 'Uva'] # Declaração de valores do array 'frutas'

# Exibe o array original
print(frutas)

# Troca de 'Manga' para 'Morango'
frutas[1] = 'Morango'
print(frutas)

# Adiciona 'Abacaxi' ao fim da lista
frutas.append('Abacaxi')
print(frutas)

# Insere o 'Abacate' no meio do array
frutas.insert(2, 'Abacate')
print(frutas)
